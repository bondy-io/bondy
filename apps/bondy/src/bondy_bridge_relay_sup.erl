-module(bondy_bridge_relay_sup).
-behaviour(supervisor).

-include("bondy.hrl").

%% API
-export([start_link/0]).


%% SUPERVISOR CALLBACKS
-export([init/1]).




%% =============================================================================
%% API
%% =============================================================================



%% -----------------------------------------------------------------------------
%% @doc
%% @end
%% -----------------------------------------------------------------------------
start_link() ->
    supervisor:start_link({local, ?MODULE}, ?MODULE, []).


%% -----------------------------------------------------------------------------
%% @doc
%% @end
%% -----------------------------------------------------------------------------
%% add_sink_sup(Name, Config) ->
