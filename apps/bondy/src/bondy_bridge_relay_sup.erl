%% =============================================================================
%% SUPERVISOR CALLBACKS
%% =============================================================================



init([]) ->
    SupFlags = #{
        strategy => one_for_one,
        intensity => 10, % max restarts
        period => 60, % seconds
        auto_shutdown => never
    },
    Children = [
        ?SUPERVISOR(bondy_bridge_relay_client_sup, [], permanent, infinity),
        ?WORKER(bondy_bridge_relay_manager, [], permanent, 5000)
    ],
    {ok, {SupFlags, Children}}.



